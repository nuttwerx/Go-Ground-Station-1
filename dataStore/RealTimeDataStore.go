func (rtDatastore *RealTimeDataStore)getField(fieldName string) (reflect.Value,error){
	value := reflect.ValueOf(rtDatastore).Elem().FieldByName(fieldName)
	var err error = nil
	if !value.IsValid() || !value.CanSet() {
		err = errors.New("field not found!")
	}
	return value, err
}

func (rtDatastore *RealTimeDataStore) setIntField(value int64, field string ){
	fieldValue, err := rtDatastore.getField(field)
	if err == nil {
		fieldValue.SetInt(value)
	}
}

func (rtDatastore *RealTimeDataStore) setUIntField(value uint64, field string ){
	fieldValue, err := rtDatastore.getField(field)
	if err == nil {
		fieldValue.SetUint(value)
	}
}

func (rtDatastore *RealTimeDataStore) setFloatField(value float64, field string ){
	fieldValue, err := rtDatastore.getField(field)
	if err == nil {
		fieldValue.SetFloat(value)
	}else{
		fmt.Printf("Error: %s\n",err.Error())
	}
}